int				check_blanc(char *buf)
{
	if (buf[0] == '\0')
		return (1);
	while (*buf)
	{
		if (*buf == COMMENT_CHAR)
			return (1);
		if (*buf != ' ' && *buf != '\t' && *buf != '\r')
			return (0);
		buf++;
	}
	return (1);
}
